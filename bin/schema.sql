CREATE TABLE IF NOT EXISTS Students(
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(200) UNIQUE,
    group_id VARCHAR(200),
    address VARCHAR(200),
    city VARCHAR(200),
    result int,
    image VARCHAR(400),
    FOREIGN KEY(group_id) REFERENCES Groups(id)
);

CREATE TABLE IF NOT EXISTS Groups(
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(200) UNIQUE
);
